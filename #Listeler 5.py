#sort()Liste nesnelerinin , listeyi varsayılan olarak artan şekilde alfasayısal olarak sıralayacak bir yöntemi vardır 
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist)


thislist = [100, 50, 65, 82, 23]
thislist.sort()
print(thislist)

#Azalan sıralama yapmak için anahtar kelime argümanını kullanın reverse = True
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort(reverse = True)
print(thislist)

thislist = [100, 50, 65, 82, 23]
thislist.sort(reverse = True)
print(thislist)
